package day1216.data;

public class ShopDto {
	// 모든 멤버 변수는 폼태그의 name과 반드시 같아야한다.
	private String sang;
	private int su;
	private int dan;
	
	public String getSang() {
		return sang;
	}
	public void setSang(String sang) {
		this.sang = sang;
	}
	public int getSu() {
		return su;
	}
	public void setSu(int su) {
		this.su = su;
	}
	public int getDan() {
		return dan;
	}
	public void setDan(int dan) {
		this.dan = dan;
	}
	
	
}
